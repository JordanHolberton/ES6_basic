#!/usr/bin/env python3
"""Module for type conversion functions, including to_str."""


def to_str(n: float) -> str:
    """
    Convert a floating-point number to its string representation.

    This function takes a floating-point number and returns its string
    representation using the built-in `str()` function.

    Parameters:
    n (float): The floating-point number to convert.

    Returns:
    str: The string representation of the number.
    """
    return str(n)
